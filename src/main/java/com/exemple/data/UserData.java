package com.exemple.data;

public class UserData {
	
	public String name;
	public String email;
	public String phone;
	public String adress;
	public String town;
	public String coment;
	
	public UserData(String name,
					String email,
					String phone,
					String adress,
					String town,
					String coment ) {
		
		this.name = name;
		this.email = email;
		this.phone = phone;
		this.adress = adress;
		this.town = town;
		this.coment = coment;
	}

}